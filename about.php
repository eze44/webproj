							<h3>About the ALLED2 project </h3>
							<p>The overall objective of the project ALLED2 is to reduce poverty through increased labour
								market participation and improved employability for the population in Kosovo.</p>

							<p>All planned actions and activities will support Kosovo’s Ministry of Education, Science
								and Technology (MEST) and Ministry of Labour and Social Welfare (MLSW) in reforming the
								education and training system to link education and training with labour market needs,
								and to reflect the European Framework of Key Competences.</p>

							<p>The specific purpose of the ALLED2 is to strengthen the quality and relevance of
								education and training programmes for the labour market and to support the adaptation of
								the legislative framework and mechanisms as a precondition for increased employability.
							</p>

							<p>Attention is on emphasizing the further step in aligning education with the labour
								market; in building, concrete cooperation models and measures between education and
								training and private sector with systemic effects. The new structured approach of
								building an education system responsive to the labour market and the many opportunities
								bringing both education and labour market representatives together, highlight innovation
								as a driving force in aligning education with labour market needs. </p>

							<p>ALLED2 project aims to support innovative structured approach based on triple helix
								principle (structured public sector – academia – business cooperation) building an
								education system, especially VET, responsive to the labor market needs and contributing
								to economic growth and development and overal employability. </p>
							<p> The specific objectives of the project, in line with the “EU Action Document for Kosovo
								2017”, Kosovo Education Strategy and Kosovo Economic Reform Agenda, are: </p>

							<p>1. Strengthen the link between Higher Education and Labour Market Needs by the
								development and modernisation of HE programmes according to labour market needs; </p>

							<p>2. Improve the teacher training programmes in order for them to conform with the
								requirements of the New Curriculum of Kosovo and the priorities of MEST;</p>

							<p>3. Enhance the quality of practical and applied teaching and learning in Vocational
								Education and Training (VET) schools and Vocational Training Centres (VTCs) in core
								occupational sectors relevant to market needs, including career guidance and
								counselling; </p>

							<p>4. Enable a better understanding of beneficiary and international qualifications and
								further develop and modernise Kosovo’s Qualification System for Qualification Framework
								professional qualifications.</p>

							<p>ALLED 2 project will support upgrading of VET programs as well as quality of teaching and
								learning processes in following sectors in Kosovo: agriculture, food processing,
								mechanical engineering and energy sector. Sectors of project interventions are selected
								based on recommendations and findings from “Labour Market Needs Analysis – Perspective
								for the future in Kosovo” (July 2019, ALLED2).</p>

